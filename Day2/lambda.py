# anonymous functions in python. A function that is defined without a name. It is also known as lambda function.
# Example:
square = lambda x: x*x   # this is a lambda function that takes an argument and returns it's square.

print(square(5))

# lambda function can take multiple arguments but can only have one expression.
# Example:
lambda x,y: x+y   # this is a lambda function that takes two arguments and returns their sum.

